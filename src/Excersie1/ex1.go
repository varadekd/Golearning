// Question : Assign the values to variables x , y and z using short declartion operator
// values to be used are 42 , "James Bond" , true
// print values using single print and multiple print statements

package main

import "fmt"

func main() {
	// Declaration of variable using short hand decalration
	x := 42
	y := "James Bond"
	z := true

	// Single print statement
	fmt.Println(x, y, z)

	// Multiple print statement
	fmt.Println(x)
	fmt.Println(y)
	fmt.Println(z)
}
